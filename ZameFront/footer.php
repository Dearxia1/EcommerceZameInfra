document.addEventListener('DOMContentLoaded', function() {
    // Luxury Shop Engine: Handle AJAX content loading
    const loadShopContent = async (url) => {
        const contentArea = document.querySelector('.woocommerce-content-wrapper');
        if (!contentArea) return;

        contentArea.classList.add('is-loading-content');
        
        try {
            const response = await fetch(url);
            const html = await response.text();
            const parser = new DOMParser();
            const doc = parser.parseFromString(html, 'text/html');

            const newContent = doc.querySelector('.woocommerce-content-wrapper');
            if (newContent) {
                // Update Content
                contentArea.innerHTML = newContent.innerHTML;
                
                // Re-initialize UI Components
                decorateShopOrdering();
                initPaginationAJAX(); // Re-bind pagination clicks
                
                // Re-initialize Animations
                document.querySelectorAll('.scroll-reveal').forEach(el => {
                    el.classList.remove('is-visible');
                    observer.observe(el);
                });

                // Update URL & Scroll to top of results
                window.history.pushState({}, '', url);
                const scrollTarget = document.querySelector('.shop-meta-controls') || contentArea;
                scrollTarget.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }
        } catch (error) {
            console.error('Luxury AJAX Error:', error);
            window.location.href = url; // Fallback
        } finally {
            contentArea.classList.remove('is-loading-content');
        }
    };

    // Intercept Pagination Clicks
    const initPaginationAJAX = () => {
        const paginationLinks = document.querySelectorAll('.woocommerce-pagination a');
        paginationLinks.forEach(link => {
            link.addEventListener('click', (e) => {
                e.preventDefault();
                loadShopContent(link.href);
            });
        });
    };

    // Custom Luxury Dropdown Decorator
    const decorateShopOrdering = () => {
        const selects = document.querySelectorAll('.woocommerce-ordering select');
        
        selects.forEach(select => {
            if (select.closest('.custom-select-wrapper')) return;

            const wrapper = document.createElement('div');
            wrapper.className = 'custom-select-wrapper';
            select.parentNode.insertBefore(wrapper, select);
            wrapper.appendChild(select);

            const display = document.createElement('div');
            display.className = 'custom-select-display';
            display.innerHTML = `<span>${select.options[select.selectedIndex].text}</span><span class="material-icons-outlined">expand_more</span>`;
            wrapper.appendChild(display);

            const menu = document.createElement('ul');
            menu.className = 'custom-select-menu';
            
            Array.from(select.options).forEach((opt, index) => {
                const li = document.createElement('li');
                li.innerText = opt.text;
                li.className = (index === select.selectedIndex) ? 'is-selected' : '';
                li.addEventListener('click', () => {
                    const newUrl = new URL(window.location.href);
                    newUrl.searchParams.set('orderby', opt.value);
                    loadShopContent(newUrl.href);
                });
                menu.appendChild(li);
            });
            
            wrapper.appendChild(menu);

            display.addEventListener('click', (e) => {
                e.stopPropagation();
                document.querySelectorAll('.custom-select-wrapper').forEach(w => {
                    if (w !== wrapper) w.classList.remove('is-active');
                });
                wrapper.classList.toggle('is-active');
            });
        });

        document.addEventListener('click', () => {
            document.querySelectorAll('.custom-select-wrapper').forEach(w => w.classList.remove('is-active'));
        });
    };

    decorateShopOrdering();
    initPaginationAJAX();

    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('is-visible');
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    document.querySelectorAll('.scroll-reveal').forEach(el => {
        observer.observe(el);
    });
});
